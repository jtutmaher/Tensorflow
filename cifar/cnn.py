        init = tf.initialize_all_variables()

        # Start running operations on the Graph.
        sess = tf.Session()#tf.Session(config=tf.ConfigProto(log_device_placement=False))
        sess.run(init)

        # Start the queue runners.
        tf.train.start_queue_runners(sess=sess)

        summary_writer = tf.train.SummaryWriter(TRAIN_DIR, sess.graph)

        for step in xrange(MAX_STEPS):
            # Set Up Batch
            start_time = time.time()
            batch_start = step*BATCH_SIZE
            batch_stop = batch_start+BATCH_SIZE
            batch_xs,batch_ys = images[batch_start:batch_stop],labels[batch_start:batch_stop]

            # Train
            _, loss_value = sess.run([train_op, loss_op],feed_dict={x:batch_xs,y_:batch_ys})
            duration = time.time() - start_time

            assert not np.isnan(loss_value), 'Model diverged with loss = NaN'

            if step % 10 == 0:
                num_examples_per_step = BATCH_SIZE
                examples_per_sec = num_examples_per_step / duration
                sec_per_batch = float(duration)

                format_str = ('%s: step %d, loss = %.2f (%.1f examples/sec; %.3f ''sec/batch)')
                print (format_str % (datetime.now(), step, loss_value,
                                 examples_per_sec, sec_per_batch))

            if step % 100 == 0:
                summary_str = sess.run(summary_op,feed_dict={x:batch_xs,y_:batch_ys})
                summary_writer.add_summary(summary_str, step)

            # Save the model checkpoint periodically.
            if step % 1000 == 0 or (step + 1) == MAX_STEPS:
                checkpoint_path = os.path.join(TRAIN_DIR, 'model.ckpt')
                saver.save(sess, checkpoint_path, global_step=step)







